void xorTable()
{
	cout << "XOR Table\n---------\n"
		<< "0 XOR 0 = " << ( 0 ^ 0 ) << '\n'
		<< "1 XOR 0 = " << ( 1 ^ 0 ) << '\n'
		<< "0 XOR 1 = " << ( 0 ^ 1 ) << '\n'
		<< "1 XOR 1 = " << ( 1 ^ 1 ) << '\n';
}// Codigo-fonte disponivel no GitHub (Caloni/Caloni.com.br) 

